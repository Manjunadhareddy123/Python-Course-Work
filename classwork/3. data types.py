# 3 Data Types

# Integer
a=10
print(a) 
print(type(a)) #10 <class 'int'>

# float
b=20.9
print(b)
print(type(b)) # 20.9 <class 'float'>

# Complex
c=3+2j
print(c)
print(type(c))# (3+2j) <class 'complex'>

# Boolean
d=True
print(d)
print(type(d)) # True <class 'bool'>

# String
e="python"
print(e)
print(type(e)) # python <class 'bool'>

f=input("Enter the f Value : ")
print(f)
print(type(f)) #Enter the f Value :manju  manju  <class 'str'> 

# List
str1 =[1,2.3,4+5j,'m',"string"]
print(str1)
print(type(str1)) # [1, 2.3, (4+5j), 'm', 'string']  <class 'list'> 

